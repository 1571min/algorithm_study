#define _CRT_SECURE_NO_WARNINGS

/*
문제
첫째 줄에는 별 1개, 둘째 줄에는 별 2개, N번째 줄에는 별 N개를 찍는 문제

입력
첫째 줄에 N(1 ≤ N ≤ 100)이 주어진다.

출력
첫째 줄부터 N번째 줄까지 차례대로 별을 출력한다.

*/
#include <cstdio>

int main() {
	int t,n=0;
	scanf("%d", &t);
	for (int i = 0; i < t-1; i++) {
		for (int j = 0; j < t+i; j++) {
			if (t == j + i+1) printf("*");
			else if (j+1 == t + i) printf("*");
			else printf(" ");
		}
		printf("\n");
	}
	
	for (int j = 0; j < 2 * t - 1; j++) {
		printf("*");
	}
	
	
	return 0;
}